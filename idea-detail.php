		<p>Акриловый камень изобретен учеными компании DuPont в 1967 году и запатентован под брендом «Кориан» в 1968 году. Его популярность связана с особенностью строения – в ней отсутствуют микропоры. Поэтому камень меньше загрязняется и исключает образование бактерий и грибка. А на ощупь – теплее натурального камня. </p>
